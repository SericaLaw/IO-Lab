
DATA SEGMENT
    COUNT DB 10
    STATE DB 1
	FLAG DB 0FFH
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA 
START:
    CLI
    MOV AX,  0080H
    MOV DS,  AX
	MOV BYTE PTR COUNT, 10
	MOV BYTE PTR STATE, 1
    ; INIT PIC
    MOV AL, 00010010B   ; ICW1
    OUT 50H, AL
    JMP $+2
    MOV AL, 00010000B   ; ICW2 = 10H
    OUT 52H, AL
    JMP $+2
    ; OCW1
    MOV AL, 00H
    OUT 52H, AL
	JMP $+2
    ; OCW2
	MOV AL, 01100000B
    OUT 50H, AL
	JMP $+2
    ; MODIFY IVT
    MOV AX, 0
    MOV ES, AX  ; START FROM 0000H						
    MOV SI, 10H * 4 ; 10H
    MOV AX, OFFSET SERVICE
    MOV WORD PTR ES:[SI], AX
    MOV AX, CS
    MOV WORD PTR ES:[SI+2], AX  

   

    ; INIT 8254
    MOV AL, 00110110B   ; T/C0 WOMODE = 3
    OUT 46H, AL
	JMP $+2
    MOV AX, 50000					
    OUT 40H, AL
	JMP $+2
    MOV AL, AH
    OUT 40H, AL
	JMP $+2
    MOV AL, 01110110B   ; T/C1
    OUT 46H, AL
	JMP $+2
    MOV AX, 200   ; 1HZ
    OUT 42H, AL
	JMP $+2
    MOV AL, AH
    OUT 42H, AL
	JMP $+2

    ; INIT 8255
    MOV AL, 10000000B;
	OUT 66H, AL
	JMP $+2

	; ENABLE DISPLAY
    MOV AL, 0FFH
    OUT 64H, AL

 
    STI
; DISPLAY8254:
;     MOV AL, 11010100B
;     OUT 46H, AL

;     IN AL, 42H
;     MOV AH, AL
;     IN AL, 42H
;     XCHG AH, AL
;     OUT 62H, AX
;     JMP DISPLAY8254
    JMP $

SERVICE PROC
    STI
    PUSH DS
    ; MOV AL, 0FFH
    ; OUT 60H, AL
	; JMP $+2
	CMP STATE, 1H
	JZ S1
	CMP STATE, 2H
	JZ S2
	CMP STATE, 3H
	JZ S3
	CMP STATE, 4H
	JZ S4
S1:
    ; GD1 (PA5), RD0 (PA2)
	MOV AL, 24H
	OUT 60H, AL
	JMP $+2
	DEC COUNT
	MOV AL, COUNT
	STI
	OUT 62H, AL ; DISPLAY
	JMP $+2
	CLI
	CMP AL, 0
	JA DONE ; COUNT > 0

	MOV COUNT, 4
	MOV STATE, 2
	JMP DONE
S2:
    ; YD1 (PA6), RD0 (PA2)
	CMP FLAG, 0FFH
	JZ S2A
    ; DISABLE YD1
	MOV AL, 04H
	OUT 60H, AL
	JMP S2B
S2A:
	MOV AL, 44H
	OUT 60H, AL
S2B:
    NOT FLAG
	DEC COUNT
	MOV AL, COUNT
	STI
	OUT 62H, AL
	JMP $+2
	CLI
	CMP AL, 0
	JA DONE

	MOV COUNT, 10
	MOV STATE, 3
	JMP DONE
S3:
    ; GD0 (PA0), RD1(PA7)
	MOV AL, 81H
	OUT 60H, AL
	DEC COUNT
	MOV AL, COUNT
	STI
	OUT 62H, AL
	JMP $+2
	CLI
	CMP AL, 0
	JA DONE

	MOV COUNT, 4
	MOV STATE, 4
	JMP DONE
S4:
    ; YD0 (PA1), RD1 (PA7)
	CMP FLAG, 0FFH
	JZ S4A

	MOV AL, 80H
	OUT 60H, AL
	JMP S4B
S4A:
	MOV AL, 82H
	OUT 60H, AL
	
S4B:
    NOT FLAG
	DEC COUNT
	MOV AL, COUNT
	STI
	OUT 62H, AL
	JMP $+2
	CLI
	CMP AL, 0
	JA DONE

	MOV COUNT, 10
	MOV STATE, 1
	JMP DONE

DONE:
	CLI

    MOV AL, 01100000B
    OUT 50H, AL
    POP DS

    IRET

SERVICE ENDP

CODE ENDS
END START
